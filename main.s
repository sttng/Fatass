;***************************************************************************
;---------------------------------------------------------------------------
; wait_vbl
; this routine waits for a vbl
;---------------------------------------------------------------------------
;***************************************************************************

wait_vbl:
	ld a,($ff40)
	add a,a
	ret nc
_waitvbl_notyet:
	ld a,($ff44)		; $ff44=LCDC Y-Pos
	cp $90			; $90 and bigger = in VBL
	jr nz,_waitvbl_notyet	; Loop until it $90
	ret

;***************************************************************************

	end
